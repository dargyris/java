public class Wall {

    private String itsDirection;

    // ============== Constructors ===========



    // =========== Setter Constructor ========

    public Wall( String direction){

    	this.itsDirection = direction;

    }

    // =======================================


    // =============== Setters ===============

    public void setItsDirection(String direction){
		this.itsDirection = direction;
	}


    // =============== Getters ===============

    public String getItsDirection(){
		return this.itsDirection;
	}


}
